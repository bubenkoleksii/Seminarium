namespace SchoolService.Application.SchoolProfile.Commands.ActivateSchoolProfile;

public class ActivateSchoolProfileCommandValidator : AbstractValidator<ActivateSchoolProfileCommand>
{
    public ActivateSchoolProfileCommandValidator()
    {
        RuleFor(x => x.Id)
            .NotNull()
            .WithErrorCode(ErrorTitles.Common.Null)
            .NotEqual(Guid.Empty)
            .WithErrorCode(ErrorTitles.Common.Empty);

        RuleFor(x => x.UserId)
            .NotNull()
            .WithErrorCode(ErrorTitles.Common.Null)
            .NotEqual(Guid.Empty)
            .WithErrorCode(ErrorTitles.Common.Empty);
    }
}
